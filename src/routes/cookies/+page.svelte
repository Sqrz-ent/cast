# 1. Introduction
This Cookie Policy explains how SQRZ uses cookies and similar tracking technologies on its websites, user profile pages, dashboard, job pages, and related services (the "Platform").
SQRZ uses cookies and similar technologies to:
- Operate and secure the Platform
- Measure performance and usage
- Support advertising and growth infrastructure
- Enable managed campaign services (SQRZ Grow)
This Cookie Policy should be read together with our Privacy Policy and Terms of Service.

* * *
‍
# 2. What Are Cookies?
Cookies are small text files stored on your device when you visit a website. They may store identifiers, session data, and user preferences.
Cookies may be:
**First-party cookies** (set by SQRZ)
**Third-party cookies** (set by external services)
In addition to cookies, we may use similar technologies such as:
- Tracking pixels
- JavaScript tags
- Local storage
- Analytics scripts
These technologies help us operate and improve the Platform.

* * *

# 3. Categories of Cookies Used by SQRZ
We use the following categories of cookies and tracking technologies:

# 3.1 Strictly Necessary Cookies
These cookies are essential for the operation of the Platform.
They enable:- Authentication and login sessions
- Account security
- Fraud prevention
- Load balancing
- Core functionality of booking and dashboard systems
Legal basis (EU): Contract performance and legitimate interest.  
These cookies do not require consent.

# 3.2 Analytics Cookies
Analytics cookies help us understand how users interact with the Platform.
They may collect information about:
- Pages visited
- Navigation patterns
- Time spent on pages
- Technical performance
Examples may include:
- Google Analytics
- Platform performance monitoring tools
Analytics cookies are activated only after user consent via our consent management mechanism ("analytics gate").

# 3.3 Marketing & Advertising Cookies
Marketing cookies are used to:
- Build retargeting audiences
- Measure campaign effectiveness
- Support SQRZ Grow advertising services
- Segment industry-specific campaign clusters
Examples may include:
- Meta Pixel
- LinkedIn Insight Tag
- Google Ads tracking
- HubSpot tracking scripts
For Grow participants, advertising tracking may operate within purpose-bound, industry- or geography-segmented campaign environments.Marketing cookies require user consent.
‍
# 3.4 Embedded Third-Party Content
User profile pages may contain embedded content from third-party services such as:
- YouTube
- Spotify
- Bandsintown
- Other media or ticketing platforms
These services may independently set cookies or collect data when content is loaded.
Where technically feasible, embedded third-party content will load only after user consent.
SQRZ does not control third-party cookie practices.

* * *
‍
# 4. Pixel Clusters & Advertising Infrastructure
For SQRZ Grow participants, SQRZ may operate segmented advertising tracking environments ("pixel clusters") designed to optimize campaign performance.
These clusters:
- Are purpose-bound
- Are not sold to third parties
- Are not mixed across unrelated industries
- Are used solely for campaign measurement and optimization
This infrastructure allows for improved advertising efficiency while maintaining separation between vertical segments.

* * *

# 5. Consent Management
SQRZ uses a consent management mechanism ("analytics gate") to allow users to:
- Accept or reject non-essential cookies
- Select cookie categories
- Withdraw consent at any time
Consent preferences can be modified via the cookie settings interface available on the Platform.
Withdrawal of consent does not affect prior lawful processing.

* * *

# 6. How to Manage Cookies
You may manage cookies in the following ways:
- Adjust settings in your browser to block or delete cookies
- Use the Platform's cookie settings interface
- Manage advertising preferences via third-party platforms such as:    
- Meta Ads settings    
- Google Ad Settings    
- LinkedIn Ad Preferences
Please note that disabling certain cookies may impact Platform functionality.

